
class Robot:
    def __init__(self, name, model):
        self.name = name
        self.model = model

def introduce(self):
    print(f"Hello! My name is {self.name}. I am a {self.model} model robot.")


robot1 = Robot("Tom", "TX-100")
robot2 = Robot("Jerry", "JX-200")


robot1.introduce()
robot2.introduce()
